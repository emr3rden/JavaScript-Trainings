// w/Concatenation Operations

// 1- Use Arithmetic Operations with Concatenation Operations in console.log

console.log("6" + 2); // output: 62 - (Concatenation)

console.log("6" - 2); // output: 4 - (Subtraction)

console.log("6" * 2); // output: 12 - (Multiplication)

console.log("6" / 2); // output: 3 - (Division)

console.log("6" % 2); // output: 0 - (Modulus - Remainder)

console.log("6" ** 2); // output: 36 - (Exponentiation - 6 ** 2 == 6 * 6)

// 2- Use Arithmetic Operations with Concatenation Operations in console.log

console.log("6" + "2" + 4); // output: 624 - (Concatenation)

console.log("1" + 2 + 3); // output: 123 - (Concatenation)

console.log(1 + "2" + 3); // output: 123 - (Concatenation)

console.log(1 + 2 + "3"); // output: 33 - (Addition + Concatenation)

console.log("6" - "2" - 2); // output: 2 - (Subtraction)

console.log("6" * "2" * 2); // output: 24 - (Multiplication)

console.log("6" / "2" / 2); // output: 1.5 - (Division)
