// "const"

// 1- Variables defined with "const" cannot be Redeclared.

// 2- Variables defined with "const" cannot be Reassigned.

// 3- Variables defined with "const" have Block Scope.

// 4- Always declare a variable with "const" when you know that the value should not be changed.

// 5- Use "const" when you declare:
// - A new Array
// - A new Object
// - A new Function
// - A new RegExp

