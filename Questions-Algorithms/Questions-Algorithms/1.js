// Declare firstName, lastName, country, city, age, isMarried, year variable and assign value to it and use the typeof operator with console.log to check different data types.

let firstName = "Emre";
console.log(typeof firstName); // output: string

let lastName = "Erden";
console.log(typeof lastName); // output: string

let country = "Turkey";
console.log(typeof country); // output: string

let city = "Ankara";
console.log(typeof city); // output: string

let age = 22;
console.log(typeof age); // output: number

let isMarried = true;
console.log(typeof isMarried); // output: boolean

let year = 2022;
console.log(typeof year); // output: number
